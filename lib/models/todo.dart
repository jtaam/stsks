class Todo {
  String id;
  String taskId;
  String createdBy;
  String doneBy;
  String todo;
  String done;
  String endDate;

  Todo({
    required this.id,
    required this.taskId,
    required this.createdBy,
    required this.doneBy,
    required this.todo,
    required this.done,
    required this.endDate,
  });
}
